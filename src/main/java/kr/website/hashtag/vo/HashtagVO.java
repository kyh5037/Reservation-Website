package kr.website.hashtag.vo;

public class HashtagVO {
	private int tag_no;
	private int tag_no_sto;
	private String tag_name;
	
	public int getTag_no() {
		return tag_no;
	}
	public void setTag_no(int tag_no) {
		this.tag_no = tag_no;
	}
	public int getTag_no_sto() {
		return tag_no_sto;
	}
	public void setTag_no_sto(int tag_no_sto) {
		this.tag_no_sto = tag_no_sto;
	}
	public String getTag_name() {
		return tag_name;
	}
	public void setTag_name(String tag_name) {
		this.tag_name = tag_name;
	}
}
